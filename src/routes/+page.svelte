	<h2 class="text-3xl font-bold">FAQ</h2>
	<h3 class="text-2xl">What is ampmod.org?</h3>
	<p>
		ampmod.org will be a website that hosts essentially every official part of the AmpMod community.
		You will be able to use the wiki, post on the forums, and even share projects on ampmod.org.
	</p>
	<h3 class="text-2xl">Why does AmpMod exist?</h3>
	<p>
		Scratch is tightly controlled by its developers at the Scratch Foundation, who target it at
		beginners. Because of this, the Scratch Team has rejected several complex features, and people
		eventually stop using it and learn languages like Python instead.
	</p>
	<p>
		However, we do believe block-based programming allows people to quickly build projects. That's
		why we made AmpMod - an advanced modification of Scratch.
	</p>
